package com.weather.accurateweatherforecast.Model.Forecast;

public class Wind{
	private double deg;
	private double speed;

	public void setDeg(double deg){
		this.deg = deg;
	}

	public double getDeg(){
		return deg;
	}

	public void setSpeed(double speed){
		this.speed = speed;
	}

	public double getSpeed(){
		return speed;
	}
}
